package devPotato777.ch02;

public class Ex01 {

	public static void main(String[] args) {
		int year = 0;
		int age = 14;
		
		System.out.println(year);
		System.out.println(age);
		
		// 변수 age의 값에 2000을 더해서 변수 year에 저장
		year = age + 2000;
		
		// 변수 age에 저장된 값을 1 증가시킨다.
		age = age + 1;
		
		System.out.println(year);
		System.out.println(age);

	}

}
